package com.divingWeb.elememts;

public class Producto {
	
	public Integer idProducto;
	public Integer cantidad;
	
	public Producto(Integer idPro, Integer cant)
	{
		idProducto = idPro;
		cantidad = cant;
	}
	
	public Integer getIdProducto()
	{
		return idProducto;
	}
	
	public Integer getCantidad()
	{
		return idProducto;
	}

}
